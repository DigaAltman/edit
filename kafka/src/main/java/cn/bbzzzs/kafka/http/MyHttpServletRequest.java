    Map<String, String> headMap = new HashMap(8);

    public MyHttpServletRequest(BufferedReader bufferedReader) {
        try {
            String firstLine = bufferedReader.readLine();
            String[] split = firstLine.trim().split(" ");
            method = split[0];
            url = split[1];
            version = split[2];

            // 获取请求头
            while (true) {
                String line = bufferedReader.readLine();
                if (line.trim().equals("")) {
                    break;
                }
                String[] split1 = line.split(":");
                headMap.put(split1[0], split1[1]);
            }
            paramMap = getUrlParams(getRequestURI());
            body = bufferedReader.readLine();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private Map<String, String> getUrlParams(String url) {
        Map<String, String> map = new HashMap();
        url = url.replace("?", ";");
        if (!url.contains(";")) {
            return map;
        }
        if (url.split(";").length > 0) {
            String[] arr = url.split(";")[1].split("&");
            for (String s : arr) {
                if (s.contains("=")) {
                    String key = s.split("=")[0];
                    String value = s.split("=")[1];
                    map.put(key, value);
                } else {
                    map.put(s, null);
                }
            }
            return map;

        } else {
            return map;
        }
    }

